--[[ 🟢 场景 A：代码行号旁的“信号灯”
* 绿色 ┃: 这一行是新加的。
* 蓝色 ┃: 这一行被修改过。
* 红色 _: 这里删掉了一些行。
* 作用: 一眼看出自上次提交以来你动了哪些地方。
🟢 场景 B：快速定位修改点
* 操作: 按 ]c 或 [c。
* 效果: 光标直接在各个修改过的“代码块 (Hunk)”之间飞跃。这比用鼠标滑轮滚来滚去找修改的地方快得多。
🟢 场景 C：这是谁写的 Bug？ (Git Blame)
* 操作: 按 <leader>ghb。
* 效果: 弹出一个浮窗，显示当前行是谁在什么时候提交的，提交信息是什么。
* 用途: 甩锅必备。
🟢 场景 D：我刚才改了什么？ (Hunk Preview)
* 操作: 按 <leader>ghp。
* 效果: 在光标处弹出一个小窗口，显示这一块代码在修改前长什么样（红绿对比）。
* 用途: 如果发现改错了，按 <leader>ghr 直接把这一小块代码回滚到原始状态，而不会影响文件中其他的修改。
 ]]
return {
	{
		"lewis6991/gitsigns.nvim",
		-- [优化] 懒加载：只有在读取文件时才加载 Git 状态
		event = { "BufReadPre", "BufNewFile" },
		config = function()
			require("configs.coding.gitsigns")
		end,
	},
}
